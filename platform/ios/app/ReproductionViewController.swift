class PhotoAnnotationView : MGLAnnotationView {
    private weak var imageView: UIImageView!
    private weak var container: UIView!

    fileprivate static let size: CGSize = CGSize(width: 54, height: 54)

    override init(reuseIdentifier: String?) {
        super.init(reuseIdentifier: reuseIdentifier)
        setup()
    }

    required init?(coder aDecoder: NSCoder) {
        super.init(coder: aDecoder)
        setup()
    }

    private func setup() {
        backgroundColor = UIColor.clear
        frame = CGRect(x: 0, y: 0, width: 44, height: 44)

        let container = UIView(frame: frame)
        container.backgroundColor = UIColor.white
        addSubview(container)
        self.container = container

        let imageView = UIImageView(frame: UIEdgeInsetsInsetRect(frame, UIEdgeInsetsMake(1, 1, 1, 1)))
        container.addSubview(imageView)
        imageView.contentMode = .scaleAspectFill
        container.autoresizingMask = [.flexibleWidth, .flexibleHeight]
        self.imageView = imageView
    }

    override func layoutSubviews() {
        super.layoutSubviews()

        scalesWithViewingDistance = false
        container.layer.cornerRadius = container.frame.width / 2.0
        container.layer.masksToBounds = true
        imageView.layer.cornerRadius = imageView.frame.width / 2.0
        imageView.layer.masksToBounds = true
    }

    func set(asset: UIImage) {
        imageView.image = asset
    }
}

