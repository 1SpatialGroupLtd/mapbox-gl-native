    override func viewDidLoad() {
        super.viewDidLoad()

        let mapView = MGLMapView(frame: view.bounds)
        mapView.autoresizingMask = [.flexibleWidth, .flexibleHeight]
        mapView.styleURL = MGLStyle.outdoorsStyleURL()

        mapView.delegate = self
        view.addSubview(mapView)
        self.mapView = mapView

        mapView.zoomLevel = 12
        mapView.centerCoordinate = CLLocationCoordinate2D(latitude: 44.530466, longitude: -72.786395)

        annotation = MGLPointAnnotation()
        annotation!.title = "Fire"
        mapView.addAnnotation(annotation)

        startAnnotation = MGLPointAnnotation()
        startAnnotation!.title = "Start"
        mapView.addAnnotation(startAnnotation)

        stopAnnotation = MGLPointAnnotation()
        stopAnnotation!.title = "Stop"
        mapView.addAnnotation(stopAnnotation)

        locationAnnotation = MGLPointAnnotation()
        locationAnnotation!.title = "Location"
        mapView.addAnnotation(locationAnnotation)

        mapView.addAnnotations(photoAnnotations)
    }

    override func viewDidAppear(_ animated: Bool) {
        super.viewDidAppear(animated)
    }

    override func viewWillDisappear(_ animated: Bool) {
        super.viewWillDisappear(animated)
//        timer?.invalidate()
    }

    @objc func mapView(_ mapView: MGLMapView, didFinishLoading style: MGLStyle) {
        changeAnnotation()
    }

    @objc func changeAnnotation() {
        mapView.removeAnnotation(polylines[currentAnnotation])
        mapView.removeAnnotation(borderPolylines[currentAnnotation])
        currentAnnotation = Int(arc4random_uniform(UInt32(polylines.count)))
        mapView.addAnnotation(borderPolylines[currentAnnotation])
        mapView.addAnnotation(polylines[currentAnnotation])

        annotation.coordinate = polylines[currentAnnotation].coordinate
        startAnnotation.coordinate = trackData[currentAnnotation].first!
        stopAnnotation.coordinate = trackData[currentAnnotation].last!

        currentLocation = 0
        timer = Timer.scheduledTimer(timeInterval: 0.0005, target: self, selector: #selector(changeLocation), userInfo: nil, repeats: true)
    }

    @objc func changeLocation() {
        currentLocation += Int(arc4random_uniform(UInt32(10))) + 1
        guard currentLocation < trackData[currentAnnotation].count else {
            timer?.invalidate()
            changeAnnotation()
            return
        }

        locationAnnotation.coordinate = trackData[currentAnnotation][currentLocation]
    }

    @objc func mapView(_ mapView: MGLMapView, viewFor annotation: MGLAnnotation) -> MGLAnnotationView? {
        guard let annotation = annotation as? PhotoAnnotation else {
            return nil
        }

        let reuseIdentifier = "Photo"
        var annotationView = mapView.dequeueReusableAnnotationView(withIdentifier: reuseIdentifier) as? PhotoAnnotationView

        // If there’s no reusable annotation view available, initialize a new one.
        if annotationView == nil {
            annotationView = PhotoAnnotationView(reuseIdentifier: reuseIdentifier)
        }
        annotationView?.set(asset: annotation.asset!)

        return annotationView
    }

    @objc func mapView(_ mapView: MGLMapView, strokeColorForShapeAnnotation annotation: MGLShape) -> UIColor {
        return annotation.title!.contains("Border") ? UIColor.white : UIColor.blue
    }

    @objc func mapView(_ mapView: MGLMapView, lineWidthForPolylineAnnotation annotation: MGLPolyline) -> CGFloat {
        return annotation.title!.contains("Border") ? CGFloat(8.5) : CGFloat(5.0)
    }

    @objc func mapView(_ mapView: MGLMapView, annotationCanShowCallout: MGLAnnotation) -> Bool {
        return false
    }

    @objc func mapView(_ mapView: MGLMapView, imageFor annotation: MGLAnnotation) -> MGLAnnotationImage? {
        guard let title = annotation.title! else {
            return nil
        }

        var imageAnnotation = mapView.dequeueReusableAnnotationImage(withIdentifier: title)
        if imageAnnotation == nil {
            if let image = UIImage(named: title) {
                imageAnnotation = MGLAnnotationImage(image: image, reuseIdentifier: title)
            }
        }
        return imageAnnotation
    }

